public class Base {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        System.out.println(" сумма a + b = " + (a + b));
        float f = 5.05f;
        float u = 5.05f;
        System.out.println(" сумма f + u = " + (f + u));
        System.out.println(" произведение a * b = " + (a * b));
        System.out.println(" произведение a * f = " + (a * f));
        System.out.println(" деление a / b = " + (a / b));
        System.out.println(" деление a % b = " + (a % b));

        int i = 1;
        System.out.println(" инкремент ++ i = " + (++i));
        System.out.println(" дикремент -- i = " + (--i));

        System.out.println(" a < b = " + (a < b));
        System.out.println(" a <= b = " + (a <= b));
        System.out.println(" a > b = " + (a > b));
        System.out.println(" a >= b = " + (a >= b));


        System.out.println(" a == b = " + (a == b));
        System.out.println(" a != b = " + (a != b));


    }
}